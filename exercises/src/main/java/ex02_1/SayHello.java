package ex02_1;

// This is our first simple Java class.
public class SayHello {

    // This is the 'main' method. It's the special starting point for our program.
    // The JVM looks for this method when you run the class.
    public static void main(String[] args) {
        // This line prints "Hello World!" to the console.
        // System.out is the standard output stream, and println() prints a line of text.
        System.out.println("Hello World!");
    }

}
